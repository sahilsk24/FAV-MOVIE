  constructor() { }

  getAllMovies(): Observable<Movie[]> {
    return of(this.movieData);
  }

  getMoviesByCategory(category: string): Observable<Movie[]> {
    return of(this.movieData.filter(movie => 
      movie.category.some(cat => cat.toLowerCase() === category.toLowerCase())));
  }

  getMovieDetails(movieName: string): Observable<Movie | undefined> {
    return of(this.movieData.find(movie => movie.name === movieName));
  }

  searchMovies(query: string): Observable<Movie[]> {
    const searchTerm = query.toLowerCase();
    return of(this.movieData.filter(movie => 
      movie.name.toLowerCase().includes(searchTerm) || 
      movie.genre.toLowerCase().includes(searchTerm) ||
      movie.language.toLowerCase().includes(searchTerm)
    ));
  }
